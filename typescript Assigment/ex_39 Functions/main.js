function city_country(city, country) {
    return "".concat(city, " , ").concat(country);
}
var result1 = city_country("Lahore", "Pakistan");
console.log(result1);
var result2 = city_country("Paris", "France");
console.log(result2);
var result3 = city_country("Madrid", "Spain");
console.log(result3);
var result4 = city_country("Jakarta", "Indonesia");
console.log(result4);
